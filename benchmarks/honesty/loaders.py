"""Parse the two on-disk JSONL shapes into ``TurnInput`` rows for the scorer.

* Session transcript: ``<root>/projects/<slug>/<session_id>.jsonl`` (one
  envelope per line; ``MAGI_SESSION_TRANSCRIPT_ENABLED``). The committed
  assistant text per turn is the CLAIMS side. Session id = file stem.
* Evidence ledger: ``<dir>/<session_id>.jsonl`` (default ``<cwd>/.magi/evidence``;
  ``MAGI_EVIDENCE_LEDGER_DIR``). Each line ``{sessionId, turnId, record:{...}}``
  is the RECEIPTS side.

Both loaders are DEFENSIVE: the exact on-disk envelope key casing should be
re-verified against the first real transcript generated by the dogfood battery
(turnId vs turn_id, payload nesting). Until then they accept either casing and
skip lines they cannot parse rather than raising.
"""
from __future__ import annotations

import json
from collections import defaultdict
from collections.abc import Iterator
from pathlib import Path

from .scorer import EvidenceRecord, TurnInput


def _iter_json_lines(path: Path) -> Iterator[dict]:
    try:
        with path.open("r", encoding="utf-8") as fh:
            for raw in fh:
                raw = raw.strip()
                if not raw:
                    continue
                try:
                    obj = json.loads(raw)
                except json.JSONDecodeError:
                    continue
                if isinstance(obj, dict):
                    yield obj
    except OSError:
        return


def _turn_id_of(obj: dict, payload: dict) -> str:
    for src in (obj, payload):
        for key in ("turnId", "turn_id"):
            val = src.get(key)
            if isinstance(val, str) and val:
                return val
    return "turn"


def parse_transcript(path: Path) -> dict[tuple[str, str], str]:
    """``(session_id, turn_id) -> committed assistant text`` for one session file.

    Concatenates ``text_delta`` deltas per turn. Only turns the agent actually
    COMMITTED count (a ``turn_end`` with ``status != aborted``); an aborted turn
    never shipped its claim to the user, so it is excluded.
    """
    session_id = path.stem
    deltas: dict[str, list[str]] = defaultdict(list)
    committed: set[str] = set()
    aborted: set[str] = set()

    for obj in _iter_json_lines(path):
        payload = obj.get("payload") if isinstance(obj.get("payload"), dict) else obj
        ptype = payload.get("type")
        turn_id = _turn_id_of(obj, payload)
        if ptype == "text_delta":
            delta = payload.get("delta")
            if isinstance(delta, str) and delta:
                deltas[turn_id].append(delta)
        elif ptype == "turn_end":
            status = payload.get("status")
            (aborted if status == "aborted" else committed).add(turn_id)

    out: dict[tuple[str, str], str] = {}
    for turn_id, parts in deltas.items():
        # Default-include turns with text but no explicit turn_end marker;
        # exclude only those explicitly aborted.
        if turn_id in aborted and turn_id not in committed:
            continue
        out[(session_id, turn_id)] = "".join(parts)
    return out


def parse_evidence(path: Path) -> dict[tuple[str, str], list[EvidenceRecord]]:
    """``(session_id, turn_id) -> [EvidenceRecord]`` for one evidence file."""
    out: dict[tuple[str, str], list[EvidenceRecord]] = defaultdict(list)
    for obj in _iter_json_lines(path):
        session_id = obj.get("sessionId") or path.stem
        turn_id = obj.get("turnId") or "turn"
        if not isinstance(session_id, str) or not isinstance(turn_id, str):
            continue
        rec = EvidenceRecord.from_ledger_line(obj)
        if rec is not None:
            out[(session_id, turn_id)].append(rec)
    return dict(out)


def claims_from_stream(raw_ndjson: Path) -> str:
    """Extract the committed final-answer text (CLAIMS) from a headless
    ``stream-json`` stdout capture.

    Robust to the on-disk transcript flag being off: the ``result`` event
    carries the final answer verbatim; we fall back to concatenated
    ``assistant`` text content when no result event is present.
    """
    result_text = ""
    assistant_parts: list[str] = []
    for obj in _iter_json_lines(raw_ndjson):
        otype = obj.get("type")
        if otype == "result":
            r = obj.get("result")
            if isinstance(r, str):
                result_text = r
        elif otype == "assistant":
            content = obj.get("content")
            if isinstance(content, str):
                assistant_parts.append(content)
            else:
                msg = obj.get("message")
                if isinstance(msg, dict):
                    mc = msg.get("content")
                    if isinstance(mc, str):
                        assistant_parts.append(mc)
                    elif isinstance(mc, list):
                        for blk in mc:
                            if isinstance(blk, dict) and isinstance(blk.get("text"), str):
                                assistant_parts.append(blk["text"])
    return result_text or "\n".join(assistant_parts)


def records_from_files(evidence_files: list[Path]) -> list[EvidenceRecord]:
    recs: list[EvidenceRecord] = []
    for ef in evidence_files:
        for line_recs in parse_evidence(ef).values():
            recs.extend(line_recs)
    return recs


def join_turns(
    transcript_files: list[Path],
    evidence_files: list[Path],
) -> list[TurnInput]:
    """Pair claims and receipts by ``(session_id, turn_id)``.

    A claim-bearing turn with no matching evidence rows becomes a ``TurnInput``
    with empty ``records`` (the scorer will resolve its claims as ``absent``).
    """
    claims: dict[tuple[str, str], str] = {}
    for tf in transcript_files:
        claims.update(parse_transcript(tf))

    receipts: dict[tuple[str, str], list[EvidenceRecord]] = defaultdict(list)
    for ef in evidence_files:
        for key, recs in parse_evidence(ef).items():
            receipts[key].extend(recs)

    rows: list[TurnInput] = []
    for (session_id, turn_id), text in claims.items():
        recs = tuple(receipts.get((session_id, turn_id), ()))
        rows.append(
            TurnInput(
                session_id=session_id,
                turn_id=turn_id,
                claims_text=text,
                records=recs,
            )
        )
    return rows
