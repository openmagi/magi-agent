/** Recursively widen all literal strings to \`string\` so locale files can use different text.
 *  Also collapses fixed-length tuples (`as const` arrays) to variable-length arrays so
 *  non-English locales can ship a different number of examples/steps without failing
 *  the structural check. */
type Widen<T> = T extends string
  ? string
  : T extends readonly (infer U)[]
    ? readonly Widen<U>[]
    : { [K in keyof T]: Widen<T[K]> };

