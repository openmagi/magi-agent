# C-6 documented strengthening — URLs whose host decodes (via legacy-IPv4
# coercion, IPv4-mapped IPv6, or NAT64) to an embedded IPv4 that lives in
# :data:`magi_agent.security.ssrf.METADATA_HOSTS`. Legacy ``sandbox/network``
# checked ``host in _METADATA_HOSTS`` AS-A-STRING (e.g. ``"0xa9fea9fe" in {...}``
# is False) so these cases got ``private_network_blocked`` only. The
# consolidation ADDITIONALLY emits ``metadata_endpoint_blocked`` because
# ``coerce_ip`` resolves the embedded IPv4 BEFORE checking the metadata set.
#
# Strict-superset direction: every legacy reason is preserved (verified by
# the loop below); the only DELTA is the addition of
# ``metadata_endpoint_blocked`` on these specific URLs — a hardening, not a
# regression. Listed explicitly so future contributors can audit at a glance.
_METADATA_STRENGTHENING_URLS: frozenset[str] = frozenset(
    {
        # Hex packed-IPv4 form of 169.254.169.254
        "http://0xa9fea9fe/",
        # IPv4-mapped IPv6 (::ffff:169.254.169.254)
        "http://[::ffff:169.254.169.254]/",
        # NAT64 (64:ff9b::169.254.169.254)
        "http://[64:ff9b::a9fe:a9fe]/",
    }
)
_EMBEDDED_METADATA_REASON = "metadata_endpoint_blocked"


@pytest.mark.parametrize("url,expected_safe_host,expected_reasons", _CLASSIFY_GOLDEN)
def test_classify_network_url_equivalence(
    url: str,
    expected_safe_host: str | None,
    expected_reasons: tuple[str, ...],
) -> None:
    """Strict-superset equivalence: every legacy reason must still appear and
    safe_host must match exactly. Extra reasons (only on the NAT64
    strengthening case) are documented."""
    from magi_agent.sandbox.network import classify_network_url

    safe_host, reasons = classify_network_url(url)
    assert safe_host == expected_safe_host, (
        f"safe_host drift on {url!r}: legacy={expected_safe_host!r} "
        f"new={safe_host!r}"
    )
    for expected in expected_reasons:
        assert expected in reasons, (
            f"REGRESSION on {url!r}: legacy emitted {expected!r}, new emits "
            f"{reasons!r}. C-6 must be strict-superset — any URL the legacy "
            f"blocked must stay blocked."
        )
    # Document allowed additions: ONLY the embedded-IPv4-metadata cases
    # (NAT64, IPv4-mapped IPv6, legacy hex-packed IPv4) may add
    # ``metadata_endpoint_blocked``. Any other added reason is a regression.
    extras = tuple(code for code in reasons if code not in expected_reasons)
    if extras:
        assert url in _METADATA_STRENGTHENING_URLS, (
            f"Unexpected extra reason on {url!r}: extras={extras!r}. C-6 only "
            f"intentionally adds reasons on the embedded-IPv4-metadata "
            f"strengthening cases: {sorted(_METADATA_STRENGTHENING_URLS)}"
        )
        assert extras == (_EMBEDDED_METADATA_REASON,), (
            f"Embedded-IPv4 metadata strengthening expected exactly "
            f"{(_EMBEDDED_METADATA_REASON,)!r}, got {extras!r}"
        )


@pytest.mark.parametrize("host,expected", _IS_BLOCKED_IP_GOLDEN)
def test_is_blocked_ip_equivalence(host: str, expected: bool) -> None:
    """``is_blocked_ip`` is byte-identical for every legacy fixture."""
    from magi_agent.web_acquisition.policy import is_blocked_ip

    assert is_blocked_ip(host) == expected, (
        f"is_blocked_ip drift on {host!r}: legacy={expected!r} "
        f"new={is_blocked_ip(host)!r}"
    )


