"""C-6/C-7 EQUIVALENCE GOLDEN — strict-superset / no silent weakening.

The C-2 lesson (a "byte-identical to legacy" docstring is not the same as
verified byte-identical behavior — Case 13 caught a silent unblocking) is the
hard rule for any security-boundary consolidation. This module captures the
LEGACY behavior of:

* :func:`magi_agent.sandbox.network.classify_network_url`
* :func:`magi_agent.web_acquisition.policy.is_blocked_ip`
* :func:`magi_agent.web_acquisition.policy.url_policy_error`

on a 20+-payload fixture set covering every legacy-fork edge case, and asserts
that the consolidated implementation produces:

* The SAME ``safe_host`` value on every URL.
* The SAME reason codes as a SUBSET of the new reason codes (strict-superset
  invariant — new reasons MAY be added on the NAT64 strengthening case, but
  no URL that was blocked before is unblocked).
* The SAME ``is_blocked_ip`` boolean on every IP.
* The SAME ``url_policy_error`` string on every URL.

Capture method: the legacy golden below was generated by running
``/tmp/capture_legacy_ssrf.py`` against the pre-C-6 codebase BEFORE any
``sandbox/network.py`` / ``web_acquisition/policy.py`` edits.
"""

